    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Author::class);
    }

        /**
     * Fetch authors based on the number page and the number of authors per page specified
     * 
     * @param int $page 
     * @param int $limit
     * @return array
     */
    public function getAllAuthors(int $page, int $limit): array
    {
        $qb = $this->createQueryBuilder('u');
        return $qb->setFirstResult(($page - 1) * $limit)
            ->setMaxResults($limit)
            ->getQuery()
            ->getResult();
    }

